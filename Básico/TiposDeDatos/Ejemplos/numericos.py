a= 10 #Entero positivo
b= -15 #Entero negativo
c= 3.14 #Real, flotante o decimal
d= 10j #Imaginario
e= 2+8j #Complejo
#La función type nos sirve para saber a qué clase pertenece un tipo de dato
print("Tipo de dato de a",type(a))
print("Tipo de dato de b",type(b))
print("Tipo de dato de c",type(c))
print("Tipo de dato de d",type(d))
print("Tipo de dato de e",type(e))

#Operaciones entre tipos de datos
print("a+b=",a+b)
print("a-b=",a-b)
print("a*c=",a*c)
print("a/b=",a/b)
print("d+e=",d+e)
print("d*e=",d*e)
print("Modulo de a",a%2) #El resultado de a%2 nos devuelve el residuo de a/2 
print('Potencia de "a" a la 2 es: ',a**2,"y esta cool")

